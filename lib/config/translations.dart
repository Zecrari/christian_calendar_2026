        : _monthsEn[monthIndex - 1];
  }

  // --- HELPER FOR WEEKDAYS ---
  static const List<String> _daysEn = ['S', 'M', 'T', 'W', 'T', 'F', 'S'];
  static const List<String> _daysTa = ['ஞா', 'தி', 'செ', 'பு', 'வி', 'வெ', 'ச'];

  static List<String> getWeekdays(String lang) {
    return (lang == 'ta') ? _daysTa : _daysEn;
  }

  static String get(String key, String lang) {
    return translations[key]?[lang] ?? translations[key]?['en'] ?? key;
  }
